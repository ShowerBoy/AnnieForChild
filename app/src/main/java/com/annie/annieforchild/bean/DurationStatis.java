public class DurationStatis {
    private String listeningDuration;
    private int listeningAverage;
    private String readingDuration;
    private int readingAverage;
    private String speakingDuration;
    private int speakingAverage;

    public String getListeningDuration() {
        return listeningDuration;
    }

    public void setListeningDuration(String listeningDuration) {
        this.listeningDuration = listeningDuration;
    }

    public int getListeningAverage() {
        return listeningAverage;
    }

    public void setListeningAverage(int listeningAverage) {
        this.listeningAverage = listeningAverage;
    }

    public String getReadingDuration() {
        return readingDuration;
    }

    public void setReadingDuration(String readingDuration) {
        this.readingDuration = readingDuration;
    }

    public int getReadingAverage() {
        return readingAverage;
    }

    public void setReadingAverage(int readingAverage) {
        this.readingAverage = readingAverage;
    }

    public String getSpeakingDuration() {
        return speakingDuration;
    }

    public void setSpeakingDuration(String speakingDuration) {
        this.speakingDuration = speakingDuration;
    }

    public int getSpeakingAverage() {
        return speakingAverage;
    }

    public void setSpeakingAverage(int speakingAverage) {
        this.speakingAverage = speakingAverage;
    }
}
